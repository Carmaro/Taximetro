    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
       
        horaBajada = Calendar.getInstance();
        formatoHoraBajada.format(horaBajada.getTime());
        dia = Calendar.getInstance();
        double latitud = Double.valueOf(jTextFieldLatitud.getText());
        double longitud = Double.valueOf(jTextFieldLongitud.getText());
        
        CoordinatesConverter conversorCoordenadas = new CoordinatesConverter();
        jTextFieldCoorLatitud.setText(conversorCoordenadas.decimalToDMS(latitud));
        jTextFieldCoorLongitud.setText(conversorCoordenadas.decimalToDMS(longitud));
        
        //Cambia los siguientes valores por las direciones de origen y destino deseadas
        String origen = jTextFieldLatitud.getText() + "," + jTextFieldLongitud.getText();
        String destino = jTextFieldDestino.getText();
        
        //Hay que sustituir los espacios por el car√°cter %20 para que se
        //  interprete bien la URL que se generar√°
        destino = destino.replaceAll(" ", "%20");
       
        //Cambia el nombre [etiqueta] por el nombre de un objeto JLabel (etiqueta) que
        //  has debido colocar antes en el formulario y que ser√° donde se muestre el mapa.
        //  Deja la etiqueta del formulario sin ning√∫n texto en el dise√±o.
        javax.swing.JLabel jLabelMapa = jLabelMapaRuta;
        
        //Indica aqu√≠ el tama√±o que desees para el mapa
        int tamHorizontal = 200;
        int tamVertical = 200;
        
        //Puedes encontrar m√°s informaci√≥n sobre el uso de mapas est√°ticos de Google Maps aqu√≠:
        //  http://code.google.com/intl/es-ES/apis/maps/documentation/staticmaps/

        //A partir de aqu√≠ no hace falta cambiar nada
        try {
            String txtDireccionImagenMapa = "http://maps.google.com/maps/api/staticmap?path="+
                    origen+"|"+destino+"&size="+tamHorizontal+"x"+tamVertical+
                    "&language=ES&sensor=false";
            System.out.println(txtDireccionImagenMapa);
            java.awt.Toolkit toolkit = java.awt.Toolkit.getDefaultToolkit();
            java.awt.Image imagenMapa = toolkit.getImage(new java.net.URL(txtDireccionImagenMapa)); 
            jLabelMapa.setIcon(new javax.swing.ImageIcon(imagenMapa));    
        } catch(java.net.MalformedURLException e) {   
            javax.swing.JOptionPane.showMessageDialog(this, "La direcci√≥n de imagen indicada no es correcta");
        }
        
        //Damos formato al ticket que aparecera en nuestro JTextArea 
        //Primero creamos el formato para dar el numero de ticket y aumentamos el contador en 1
        DecimalFormat ticket = new DecimalFormat("00000");
        contador++;
        String numeroTicket = ticket.format(contador);
        
        //Mostramos los resultados del ticket
       
        jTextAreaDatos.setText("TICKET\n");
        jTextAreaDatos.append("======\n");
        jTextAreaDatos.append("N¬∫ticket: " + numeroTicket);
        jTextAreaDatos.append("\nFecha:" + formatoFechaBajada.format(dia.getTime()));
        jTextAreaDatos.append("\n\n");
        jTextAreaDatos.append("Hora bajada de bandera: " + formatoHoraBajada.format(horaBajada.getTime()));

    }//GEN-LAST:event_jButtonStartActionPerformed

    private void jButtonFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFinishActionPerformed
        
        
        Calendar horaSubida = Calendar.getInstance();
        
        long horaFin = horaBajada.getTimeInMillis();
        long horaInicio = horaSubida.getTimeInMillis();
        long duracionTrayecto = horaInicio - horaFin;
        
        //cambiamos el formato para que nos de la hora en "HH-MM-SS"
        String formatoHora = DurationFormatUtils.formatDuration(duracionTrayecto,"HH:mm:ss");
        
        //Damos formato de moneda en Euros 
        NumberFormat formatoImporte = NumberFormat.getCurrencyInstance(new Locale("es","ES"));
        
        //Calculamos el iva , el importe y el importe total 
        double duracionSegundos = duracionTrayecto/1000; 
        double importe = (duracionSegundos/60)*TARIFA;
        double iva = importe * IVA; 
        double importeTotal = importe + iva;
        
        //int duracion = horaFinViaje - horaBajada;
        jTextAreaDatos.append("\nHora Fin de trayecto : " + formatoHoraBajada.format(horaSubida.getTime()));  
        jTextAreaDatos.append("\nDuracion de trayecto : " + formatoHora );
        jTextAreaDatos.append("\nTarifa por minuto : " + formatoImporte.format(TARIFA ));
        jTextAreaDatos.append("\n");
        jTextAreaDatos.append("\nImporte : " + formatoImporte.format(importe));
        jTextAreaDatos.append("\nIVA : " + formatoImporte.format(iva));
        jTextAreaDatos.append("\nImporte total : " + formatoImporte.format(importeTotal));
        
    }//GEN-LAST:event_jButtonFinishActionPerformed

